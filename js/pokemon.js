/**
 * This functions generates a little table that shows the pokemon.
 */
function generatePokemonCardHtml(pokemonId, chanceLiteral){
	var pokemons = getDefaultPokemons();
	var pokemon = pokemons[pokemonId-0];
	var pokemonId = parseInt(pokemon[0]);
	var html = "";
	html += `<div id="poke-${pokemonId}" class="poke-card disable-select" onClick="clickedPokemon(${pokemonId})">`;
	html += 	`<div class="poke-card-id">${pokemon[0]}</div>`;
	html += 	`<div class="poke-card-icon"><img src="img/icon/${pokemon[0]}.png"/></div>`;
	html += 	`<div class="poke-card-name">${pokemon[1]}</div>`;
	html += 	`<div class="poke-card-type"><span class="poke-type ${pokemon[2]}-bg">${pokemon[3]}</span>`;
	if(pokemon[4] != undefined) {
		html +=	`<span class="poke-type ${pokemon[4]}-bg">${pokemon[5]}</span></div>`;
	} else {
		html += `</div>`;
	}
	html += 	`<div class="poke-card-chance">${chanceLiteral}</div>`;
	html += `</div>`;
	return html;
}

