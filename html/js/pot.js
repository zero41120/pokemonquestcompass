function getIngredientAttribute(ingredients) {
	var attr = {
		r: 0,
		b: 0,
		y: 0,
		g: 0,
		big: 0,
		hard: 0,
		soft: 0,
		shroom: 0,
		veg: 0,
		mineral: 0,
		sweet: 0,
		god: 0
	}
	for (var i = 0; i < ingredients.length; i++) {
		var ing = ingredients[i];
		// Size
		if (ing.charAt(0) == 'b') {
			attr.big += 1;
		}
		if (ing == 'sm') {
			attr.big += 2;
		}
		if (ing == 'bm') {
			attr.god += 1;
		}
		// Color
		attr[ing.charAt(1)] += 1;
		// Hardness
		if (ing in {
				sy: 0,
				sg: 0,
				bb: 0
			}) {
			attr.hard += 1;
		}
		if (ing in {
				sr: 0,
				sb: 0,
				br: 0,
				bg: 0,
				by: 0
			}) {
			attr.soft += 1;
		}
		// Type
		if (ing in {
				sr: 0,
				bg: 0
			}) {
			attr.shroom += 1;
		}
		if (ing in {
				sy: 0,
				br: 0
			}) {
			attr.veg += 1;
		}
		if (ing in {
				sg: 0,
				bb: 0
			}) {
			attr.mineral += 1;
		}
		if (ing in {
				sb: 0,
				by: 0
			}) {
			attr.sweet += 1;
		}
	}
	return attr;
}
var identifyPod = function (attr) {
	// Color > Type > Index > Legend > Mug
	var goodness = "";
	if (attr.big == 0) {
		goodness = "(普通)"
	} else if (attr.big <= 2) {
		goodness = "(好吃)"
	} else if (attr.big <= 4) {
		goodness = "(超好吃)"
	} else if (attr.big >= 5) {
		goodness = "(好吃到不行)"
	}

	var pots = getDefaultPots();
	for (var i = 0; i < pots.length; i++) {
		pots[i][1] += goodness;
	}

	if (attr.r >= 4) {
		return pots[1];
	}
	if (attr.b >= 4) {
		return pots[2];
	}
	if (attr.y >= 4) {
		return pots[3];
	}
	if (attr.g >= 4) {
		return pots[4];
	}

	if (attr.soft >= 4 && attr.b >= 3) {
		return pots[5];
	}
	if (attr.sweet >= 3 && attr.g >= 2) {
		return pots[6];
	}
	if (attr.shroom >= 4 && attr.soft >= 3) {
		return pots[7];
	}
	if (attr.soft >= 3 && attr.mineral >= 2) {
		return pots[8];
	}
	if (attr.veg >= 4 && attr.soft >= 2) {
		return pots[9];
	}
	if (attr.sweet >= 4 && attr.y >= 3) {
		return pots[10];
	}
	if (attr.sweet >= 3 && attr.hard >= 2) {
		return pots[11];
	}
	if (attr.hard >= 4 && attr.mineral >= 2) {
		return pots[12];
	}
	if (attr.mineral >= 3 && attr.veg >= 2) {
		return pots[13];
	}
	if (attr.shroom >= 3 && attr.r >= 2) {
		return pots[14];
	}
	if (attr.soft >= 4 && attr.y >= 3) {
		return pots[15];
	}
	if (attr.sweet >= 3 && attr.shroom >= 2) {
		return pots[16];
	}
	if (attr.god >= 1) {
		return pots[17];
	}

	return pots[0];
}

